# 3. Peça ao usuário para digitar três valores inteiros e imprima a soma deles.

num1 = int(input("Informe o primeiro numero inteiro: \n"))
num2 = int(input("Informe o segundo numero inteiro: \n"))
num3 = int(input("Informe o terceiro numero inteiro: \n"))

result = num1 + num2 + num3

print(num1, "+", num2, "+",  num3, "=", result)
